            <p>Verraki Partners, a business solutions company focused on accelerating the development and transformation of Africa has outlined solutions that can bridge the knowledge gap and expedite mass affordable, accessible and quality education for most Nigerians. Speaking during the sidelines at the grand finale of the national design competition, Professor Ayodele Awojobi Design Competition (PAADC) held at the University of Lagos recently, Kelvin Balogun, Senior Partner and Head of the Ventures Unit of Verraki, asserted that the reason why Nigeria has seemingly intractable challenges is because the country has not sufficiently applied new technology thinking to them.</p>
            <p>Spotlighting Nigerian education, Balogun opined that the frayed education system in Nigeria can prove to spur innovation in how to deliver ad-hoc education, especially to the country’s lost generation. He noted that building brick and mortar schools may never catch up to Nigeria’s rapidly expanding student population and proposed a new way to tackle this aged problem.</p>
            <h1>" Verraki is focused on implementing technology and business solutions designed inherently for Africa."</h1>
            <p>“We can tackle our education problem by applying new thinking, leveraging technology such as the mobile phone and broadband internet. According to the Nigerian Communications Commission, internet subscribers in Nigeria are about 114 million as at February 2019, largely via mobile phones. The mobile phone has become the preferred device of choice for payments, ride-hailing and healthcare in Nigeria; it can be used for education and learning too. Education via mobile phones is a leveler and will guarantee access to quality education for everyone, irrespective of financial status or social class, language or tribe. It also ensures access to low-cost teaching resources, added value compared to traditional teaching and a complementary solution for teacher training”.</p>
